    public static List<QuizQuestion> DrawQuestionsForDay(int day, int count)
    {
        int safeDay = UnityEngine.Mathf.Clamp(day, 1, DaysInGame);
        int safeCount = UnityEngine.Mathf.Clamp(count, 1, QuestionsPerDay);
        int startIndex = (safeDay - 1) * QuestionsPerDay;

        List<QuizQuestion> result = new List<QuizQuestion>(safeCount);
        for (int i = 0; i < safeCount && startIndex + i < AllQuestions.Count; i++)
        {
            result.Add(CreateQuestionWithShuffledOptions(AllQuestions[startIndex + i], startIndex + i));
        }

        return result;
    }

    /// <summary>
    /// 保留随机抽题接口，方便独立测试场景或以后复用。
    /// </summary>
    public static List<QuizQuestion> DrawRandomQuestions(int count)
    {
        count = UnityEngine.Mathf.Clamp(count, 1, AllQuestions.Count);

        List<QuizQuestion> pool = new List<QuizQuestion>(AllQuestions);
        List<QuizQuestion> result = new List<QuizQuestion>(count);

        for (int i = 0; i < count; i++)
        {
            int index = UnityEngine.Random.Range(0, pool.Count);
            result.Add(pool[index]);
            pool.RemoveAt(index);
        }

        return result;
    }

    private static QuizQuestion CreateQuestionWithShuffledOptions(QuizQuestion source, int questionIndex)
    {
        string correctOption = source.options[source.correctIndex];
        List<string> wrongOptions = new List<string>(3);
        for (int i = 0; i < source.options.Length; i++)
        {
            if (i != source.correctIndex)
                wrongOptions.Add(source.options[i]);
        }

        // 固定种子洗牌：同一道题每次位置一致，但整体 A/B/C/D 分布更均匀。
        int seed = questionIndex * 37 + 17;
        int targetCorrectIndex = PositiveMod(seed, 4);
        RotateWrongOptions(wrongOptions, PositiveMod(seed / 4, wrongOptions.Count));

        string[] shuffled = new string[4];
        int wrongCursor = 0;
        for (int i = 0; i < shuffled.Length; i++)
        {
            if (i == targetCorrectIndex)
                shuffled[i] = correctOption;
            else
                shuffled[i] = wrongOptions[wrongCursor++];
        }

        return new QuizQuestion(source.question, shuffled, targetCorrectIndex, source.explanation);
    }

    private static void RotateWrongOptions(List<string> options, int offset)
    {
        if (offset == 0) return;

        List<string> copy = new List<string>(options);
        for (int i = 0; i < options.Count; i++)
        {
            options[i] = copy[(i + offset) % copy.Count];
        }
    }

    private static int PositiveMod(int value, int modulo)
    {
        int result = value % modulo;
        return result < 0 ? result + modulo : result;
    }

    private static QuizQuestion Q(
        string question,
        string optionA,
        string optionB,
        string optionC,
        string optionD,
        int correctIndex,
        string explanation)
    {
        return new QuizQuestion(
            question,
            new[] { optionA, optionB, optionC, optionD },
            correctIndex,
            explanation
        );
    }
